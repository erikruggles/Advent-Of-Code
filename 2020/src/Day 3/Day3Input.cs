        public static BiomeSquare[,] ParseInput()
        {
            var lines = Input.Split("\r\n");

            var result = new BiomeSquare[lines.Length, lines[0].Length];

            for (int i = 0; i < lines.Length; i++)
            {
                var line = lines[i];
                for (int j = 0; j < line.Length; j++)
                {
                    result[i, j] = new BiomeSquare { IsTree = line[j] == '#' };
                }
            }

            return result;
        }
